from google import genai
from google.genai import types
import os
import json
from typing import Optional, List, Dict
from datetime import datetime
from dotenv import load_dotenv

load_dotenv()

EVAL_PROMPT = """You are an expert financial planner and prompt engineer. Your task is to evaluate the output of a "Planner Agent" that generates a Python function `execute_plan` to address user requests.

### INPUTS
1. **EVAL_INPUT**: This is the prompt provided to the Planner Agent. It includes:
   - **Last User Request**: The specific request the agent must address.
   - **Previous Conversation**: Context from earlier in the chat.
2. **AGENT_OUTPUT**: The Python code (`execute_plan`) generated by the Planner Agent.
3. **PAST_REVIEW_OUTCOMES**: Reference reviews of similar cases to ensure consistency.

### EVALUATION CRITERIA
Evaluate the `AGENT_OUTPUT` based on the following rules from the Planner Agent's system prompt:

1. **Correct Skill Selection**:
   - `app_usage_info`: Used ONLY for navigation, UI, and general app questions (no user data).
   - `lookup_user_accounts_transactions_income_and_spending_patterns`: Used for ANY user data (balances, transactions, spending, income). MUST be called first if data is needed.
   - `research_and_strategize_financial_outcomes`: Used for complex analysis, long-term planning, or "what-if" scenarios. DO NOT use if `lookup` alone suffices.
   - `create_budget_or_goal`: Used ONLY when the user explicitly asks to *create* or *set up* a budget/goal.
   - `update_transaction_category_or_create_category_rules`: Used for recategorization or rule creation.

2. **Efficiency & Chaining**:
   - Do not chain `lookup` with `research` if `lookup` can answer the question (e.g., "how's my accounts doing?").
   - Ensure `input_info` is passed correctly between chained skills.

3. **Context Awareness**:
   - The plan must prioritize the **Last User Request**.
   - It should use **Previous Conversation** for context but not for stale data.

4. **No-Action / Unsupported Requests**:
   - If the request is unsupported (e.g., "remind me to cancel..."), the agent should return `(True, polite message)` without calling skills.
   - If it's just a "thanks" or "ok", it should return a brief acknowledgment.

### OUTPUT FORMAT
Return your evaluation in the following JSON format:
{
  "is_correct": bool,
  "score": int (0-10),
  "reasoning": "Detailed explanation of why the output is correct or incorrect, citing specific rules.",
  "suggested_fix": "If incorrect, provide the corrected `execute_plan` code or specific changes needed."
}
"""

class PlannerReviewOptimizer:
    def __init__(self, model_name: str = "gemini-flash-lite-latest"):
        api_key = os.getenv("GEMINI_API_KEY")
        if not api_key:
            raise ValueError("GEMINI_API_KEY not set.")
        self.client = genai.Client(api_key=api_key)
        self.model_name = model_name
        
    def evaluate(self, eval_input: str, agent_output: str, past_review_outcomes: str = "") -> Dict:
        prompt = f"""
### EVAL_INPUT
{eval_input}

### AGENT_OUTPUT
{agent_output}

### PAST_REVIEW_OUTCOMES
{past_review_outcomes}
"""
        
        response = self.client.models.generate_content(
            model=self.model_name,
            contents=[types.Content(role="user", parts=[types.Part.from_text(text=prompt)])],
            config=types.GenerateContentConfig(
                system_instruction=EVAL_PROMPT,
                temperature=0.0, # Keep it deterministic for evaluation
                response_mime_type="application/json"
            )
        )
        
        return json.loads(response.text)

